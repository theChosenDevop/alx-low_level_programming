#include "3-calc.h"

/**
 * op_add - add integers
 * @a: input
 * @b: input
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: input
 * @b: input
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: input
 * @b: input
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two integers
 * @a: input
 * @b: input
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - ramainder of division of two integers
 * @a: input
 * @b: input
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
