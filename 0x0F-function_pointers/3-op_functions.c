#include "3-calc.h"

/**
* op_add - Sum up two integers
* @a: input
* @b: input
* Return: sum of a and b
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - calculate the diffrences between two  integers
* @a: input
* @b: input
* Return: subtraction of a and b
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - calculates teh product of two integers
* @a: input
* @b: input
* Return: product of a and b
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - calculates teh divisipon of two integers
* @a: input
* @b: input
* Return: result of division of a and b
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - calculates the remainder of division of two integers
* @a: input
* @b: input
* Return: remainder of a divided b
*/

int op_mod(int a, int b)
{
	return (a % b);
}
