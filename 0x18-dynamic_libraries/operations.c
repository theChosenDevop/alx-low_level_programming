#include "operations.h"
#include <stdio.h>

/**
 * add - adds two arguments
 * @a: input
 * @b: input
 * Return: output
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two arguments
 * @a: input
 * @b: input
 * Return: output
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod - modulo of  two arguments
 * @a: input
 * @b: input
 * Return: output
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * div - div two arguments
 * @a: input
 * @b: input
 * Return: output
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multiply two arguments
 * @a: input
 * @b: input
 * Return: output
 */
int mul(int a, int b)
{
	return (a * b);
}
